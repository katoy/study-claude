    /**
     * Define the model's default state.
     *
     * @return array<string, mixed>
     */
    public function definition(): array
    {
        $isMale = $this->faker->boolean();
        $lastName = $this->faker->randomElement(self::$lastNames);
        $firstName = $this->faker->randomElement($isMale ? self::$maleFirstNames : self::$femaleFirstNames);
        $name = "{$lastName} {$firstName}";

        $email = 'user'.$this->faker->unique()->numberBetween(1000, 9999).'@example.com';

        /** @var array{subject: string, body: string} $template */
        $template = $this->faker->randomElement(self::$inquiryTemplates);
        $orderNo = 'ORD-'.$this->faker->numberBetween(20260000, 20269999);

        $subject = str_replace(['【NAME】', '【ORDER_NO】'], [$name, $orderNo], $template['subject']);
        $body = str_replace(['【NAME】', '【ORDER_NO】'], [$name, $orderNo], $template['body']);

        return [
            'name' => $name,
            'email' => $email,
            'subject' => $subject,
            'body' => $body,
            'status' => ContactStatus::New,
        ];
    }
